IF OBJECT_ID('dbo.Ports') IS NULL
CREATE TABLE dbo.Ports (
    PortID    INT IDENTITY(1,1) PRIMARY KEY,
    PortName  NVARCHAR(100) NOT NULL,
    CountryID INT NOT NULL REFERENCES dbo.Countries(CountryID),
    Notes     NVARCHAR(500) NULL,
