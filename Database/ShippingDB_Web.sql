/* =====================================================================
   ShippingDB — Web Edition (rebuilt from legacy WinForms schema + Requirement Doc V2)
   Target: Microsoft SQL Server 2016+
   NOTE: Your original ShippingDataBase.sql was not in the archive (only a .lnk
   shortcut). This script recreates and EXTENDS the schema used by the old
   Shippingrepository.cs, adding all V2 modules:
     - Users & session-based roles
     - Companies w/ Regular/Non-Regular status
     - Vessels w/ Confirm / Purchase / Catering / Generate / DeckEng emails
     - Vessel Types (seeded)
     - Countries / Ports / Port Sources (Ports Setup module)
     - Port Assignments (one port -> one user, no duplicates)
     - ScrapedData (Import Data staging, "Useless" handling, per-user distribution)
     - UselessVessels (global IMO ignore list)
     - ArrivalLog (date-wise history, Daily Report source)
   ===================================================================== */

IF DB_ID('ShippingDB') IS NULL
    CREATE DATABASE ShippingDB;
GO
USE ShippingDB;
GO

/* ───────────────────────── USERS ───────────────────────── */
IF OBJECT_ID('dbo.Users') IS NULL
CREATE TABLE dbo.Users (
    UserID        INT IDENTITY(1,1) PRIMARY KEY,
    Username      NVARCHAR(50)  NOT NULL UNIQUE,
    PasswordHash  NVARCHAR(200) NOT NULL,           -- PBKDF2 (iterations.salt.hash) generated by app
    FullName      NVARCHAR(100) NOT NULL,
    Role          NVARCHAR(20)  NOT NULL DEFAULT 'User',   -- 'Admin' | 'User'
    IsActive      BIT           NOT NULL DEFAULT 1,
    CreatedAt     DATETIME2     NOT NULL DEFAULT SYSUTCDATETIME()
);
GO
/* The application seeds a default admin (admin / Admin@123) on first run
   if this table is empty — see Program.cs. */

/* ───────────────────────── COMPANIES ───────────────────────── */
IF OBJECT_ID('dbo.Companies') IS NULL
