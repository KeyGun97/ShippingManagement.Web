    Status       NVARCHAR(20)  NOT NULL DEFAULT 'Non-Regular',  -- 'Regular' | 'Non-Regular'
    CreatedAt    DATETIME2     NOT NULL DEFAULT SYSUTCDATETIME()
);
GO

