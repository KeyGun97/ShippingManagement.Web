/* ───────────────────────── VIEWS ───────────────────────── */
IF OBJECT_ID('dbo.vw_ArrivalDetail') IS NOT NULL DROP VIEW dbo.vw_ArrivalDetail;
GO
CREATE VIEW dbo.vw_ArrivalDetail AS
SELECT al.LogID, al.IMO_Number, al.PortName, al.Country, al.ArrivalDate, al.IsTagged, al.EnteredBy,
       v.VesselName, vt.TypeName AS VesselType, v.CallSign, v.Status,
       v.ConfirmEmail, v.PurchaseEmail, v.CateringEmail, v.GenerateEmail, v.DeckEngEmail, v.GeneralEmail,
       v.Terms,
       c.CompanyID, c.CompanyName, c.Address AS CompanyAddress, c.Country AS CompanyCountry,
       c.GeneralEmail AS CompanyEmail, c.Telephone, c.Website, c.Status AS CustomerStatus
FROM dbo.ArrivalLog al
LEFT JOIN dbo.Vessels    v  ON v.IMO_Number = al.IMO_Number
LEFT JOIN dbo.VesselTypes vt ON vt.TypeID    = v.VesselTypeID
LEFT JOIN dbo.Companies  c  ON c.CompanyID  = v.CompanyID;
GO

IF OBJECT_ID('dbo.vw_CompanyFleet') IS NOT NULL DROP VIEW dbo.vw_CompanyFleet;
GO
CREATE VIEW dbo.vw_CompanyFleet AS
SELECT c.CompanyID, c.CompanyName, c.Address, c.Country, c.GeneralEmail,
       c.Website, c.Telephone, c.Status,
       COUNT(v.IMO_Number) AS FleetCount
FROM dbo.Companies c
LEFT JOIN dbo.Vessels v ON v.CompanyID = c.CompanyID
GROUP BY c.CompanyID, c.CompanyName, c.Address, c.Country, c.GeneralEmail,
         c.Website, c.Telephone, c.Status;
GO

