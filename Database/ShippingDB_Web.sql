    GenerateEmail NVARCHAR(400) NULL,
    DeckEngEmail  NVARCHAR(400) NULL,
    CateringEmail NVARCHAR(400) NULL,
    PurchaseEmail NVARCHAR(400) NULL,
    GeneralEmail  NVARCHAR(400) NULL,
