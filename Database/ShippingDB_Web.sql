);
GO

IF OBJECT_ID('dbo.PortSources') IS NULL
CREATE TABLE dbo.PortSources (
    SourceID         INT IDENTITY(1,1) PRIMARY KEY,
    PortID           INT NOT NULL REFERENCES dbo.Ports(PortID) ON DELETE CASCADE,
    SourceName       NVARCHAR(60)  NOT NULL,    -- 'MyShipTracking' | 'VesselFinder' | 'VesselTracker' | other
    Url              NVARCHAR(500) NOT NULL,
    PageParamPattern NVARCHAR(200) NULL,        -- e.g. '&page={n}' — pagination differs per site
    StartPage        INT NOT NULL DEFAULT 1,
    EndPage          INT NOT NULL DEFAULT 50,
    IsActive         BIT NOT NULL DEFAULT 1
);
GO

/* ───────────────────────── PORT ASSIGNMENTS (Admin → User) ─────────────── */
IF OBJECT_ID('dbo.PortAssignments') IS NULL
CREATE TABLE dbo.PortAssignments (
    AssignmentID INT IDENTITY(1,1) PRIMARY KEY,
